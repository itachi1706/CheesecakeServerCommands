@SuppressWarnings("unused")
public class SampleCommand implements ICommand {

    private List<String> aliases;

    public SampleCommand(){
        this.aliases = new ArrayList<>();
        this.aliases.add("sample");
        this.aliases.add("sam");
    }

    @Override
    @Nonnull
    public String getName() {
        return "sample";
    }

    @Override
    @Nonnull
    public String getUsage(@Nonnull ICommandSender sender) {
        return "/sample <text>";
    }

    @Override
    @Nonnull
    public List<String> getAliases() {
        return this.aliases;
    }

    @Override
    public void execute(@Nonnull MinecraftServer server, @Nonnull ICommandSender iCommandSender, @Nonnull String[] args) {

        if(args.length == 0)
        {
            ChatHelper.sendMessage(iCommandSender, "Invalid arguements");
            return;
        }

        ChatHelper.sendMessage(iCommandSender, "Sample: " + TextFormatting.AQUA + "[" + args[0] + "]");

    }

    @Override
    @Nonnull
    public List<String> getTabCompletions(@Nonnull MinecraftServer server, @Nonnull ICommandSender sender, @Nonnull String[] args, @Nullable BlockPos pos) {
        return Collections.emptyList();
    }

    @Override
    public boolean checkPermission(@Nonnull MinecraftServer server, @Nonnull ICommandSender iCommandSender) {
        return PlayerMPUtil.isOperatorOrConsole(iCommandSender);
    }

    @Override
    public boolean isUsernameIndex(@Nonnull String[] p_82358_1_, int p_82358_2_) {
        return false;
    }

    @SuppressWarnings("NullableProblems")
    @Override
    public int compareTo(ICommand o) {
        return 0;
    }
}
